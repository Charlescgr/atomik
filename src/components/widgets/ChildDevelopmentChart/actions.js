function myData(quantity, position, value) {
  const data = [];
  // eslint-disable-next-line no-plusplus
  for (let index = 0; index <= quantity; index++) {
    if (index === position) {
      data.push(value);
    } else {
      data.push(null);
    }
  }
  return data;
}

function yourPercentile(series, myValue, position) {
  const defaultValues = series.map((s) => s[position]);
  let lo;
  let indexP;

  defaultValues.forEach((el) => {
    let res = myValue - el;
    if (res < 0) res *= -1;
    if (lo === undefined || lo > res) {
      lo = res;
      indexP = el;
    }
  });

  const percentilPosition = defaultValues.indexOf(indexP);

  if (percentilPosition === 0) {
    return '3%';
  }
  if (percentilPosition === 1) {
    return '15%';
  }
  if (percentilPosition === 2) {
    return '50%';
  }
  if (percentilPosition === 3) {
    return '85%';
  }
  if (percentilPosition === 4) {
    return '97%';
  }

  return '0%';
}

const DateDiff = {
  inWeeks(d1, d2) {
    const t2 = d2.getTime();
    const t1 = d1.getTime();
    return parseInt((t2 - t1) / (24 * 3600 * 1000 * 7), 10);
  },
  inMonths(d1, d2) {
    const d1Y = d1.getFullYear();
    const d2Y = d2.getFullYear();
    const d1M = d1.getMonth();
    const d2M = d2.getMonth();
    // eslint-disable-next-line no-mixed-operators
    return d2M + 12 * d2Y - (d1M + 12 * d1Y);
  },
};

